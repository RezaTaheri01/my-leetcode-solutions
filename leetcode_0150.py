class Solution:
    def evalRPN(self, tokens: list[str]) -> int:
        stack: list = []

        for char in tokens:
            if char in "+-*/":
                val2, val1 = stack.pop(), stack.pop()
                if char == "+":
                    val1 += val2
                elif char == "-":
                    val1 -= val2
                elif char == "*":
                    val1 *= val2
                else:
                    val1 /= val2
                stack.append(int(val1))

            else:
                stack.append(int(char))

        return int(stack[-1])


